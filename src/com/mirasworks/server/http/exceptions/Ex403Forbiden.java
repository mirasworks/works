package com.mirasworks.server.http.exceptions;

public class Ex403Forbiden extends FastRuntimeException {

	private static final long serialVersionUID = -7502743533860010266L;

	public Ex403Forbiden() {
		super();
	}

	public Ex403Forbiden(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public Ex403Forbiden(String arg0) {
		super(arg0);
	}

	public Ex403Forbiden(Throwable arg0) {
		super(arg0);
	}

}
